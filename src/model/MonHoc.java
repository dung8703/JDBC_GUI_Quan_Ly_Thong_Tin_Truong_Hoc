package model;

/**
 *
 * @author PC ACER
 */
public class MonHoc {
    private String maMH;
    private String tenMH;
    private int soTiet;

    public MonHoc() {
        
    }

    public String getMaMH() {
        return maMH;
    }

    public void setMaMH(String maMH) {
        this.maMH = maMH;
    }

    public String getTenMH() {
        return tenMH;
    }

    public void setTenMH(String tenMH) {
        this.tenMH = tenMH;
    }

    public int getSoTiet() {
        return soTiet;
    }

    public void setSoTiet(int soTiet) {
        this.soTiet = soTiet;
    }
    
}
